/**
 * Функция складывает 2 числа
 * @param a {number} первое число
 * @param b {number} второе число
 * @return {number} результат сложения
 */
function sum(a, b) {
    return a + b;
}

sum(5, 4);


/**
 * Функция вычитает 2 числа
 * @param a {number} первое число
 * @param b {number} второе число
 * @return {number} результат вычитания
 */
function subtract(a, b) {
    return a - b;
}

subtract(7, 3);

/**
 * Функция умножает 2 числа
 * @param a {number} первое число
 * @param b {number} второе число
 * @return {number} результат умножения
 */
function multiply(a, b) {
    return a * b;
}

multiply(3, 5);

/**
 * Функция делит 2 числа
 * @param a {number} первое число
 * @param b {number} второе число
 * @return {number} результат деления
 */
function divide(a, b) {
    return a / b;
}

divide(6, 3);